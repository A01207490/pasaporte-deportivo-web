            $users = $this->search();
        } else {
            $users = User::paginate(5);
        }
        return view('users.index', compact('users'));
    }

    public function search()
    {
        $value = '%' . request('query') . '%';
        $users = User::where('name', 'LIKE', $value)
            ->orWhere('email', 'LIKE', $value)
            ->paginate(10);
        return $users;
    }

